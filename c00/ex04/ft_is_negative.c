#include <unistd.h>

void ft_putchar(char c) {
	write(1,&c,1);
}

void ft_is_negative(int n)
{
	if ( n <= 2147483647 && n > -2147483648 )
	{
		if ( n < 0 ) 
		{
			ft_putchar('N');
		}
		else
		{
			ft_putchar('P');
		}
	}
}
